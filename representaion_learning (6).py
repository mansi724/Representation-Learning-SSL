"""## Section 4 — MAE Pipeline"""

import torch
import torch.nn as nn

class PatchEmbedding(nn.Module):
    def __init__(self, image_size=224, patch_size=16, in_channels=3, embed_dim=192):
        super().__init__()

        self.patch_size  = patch_size
        self.num_patches = (image_size // patch_size) ** 2  # 196

        self.projection = nn.Conv2d(
            in_channels,   # 3 (RGB)
            embed_dim,     # 192 — each patch becomes a 192-dim vector
            kernel_size=patch_size,
            stride=patch_size
        )

    def forward(self, x):
        # x: [B, 3, 224, 224]
        x = self.projection(x)   # [B, 192, 14, 14]
        x = x.flatten(2)         # [B, 192, 196]  — flatten the 14x14 grid
        x = x.transpose(1, 2)    # [B, 196, 192]  — sequence of 196 patch vectors
        return x

class RandomMasking(nn.Module):
    def __init__(self, mask_ratio=0.75):
        super().__init__()
        self.mask_ratio = mask_ratio

    def forward(self, x):
        """
        x: [B, 196, 192] — full sequence of patch embeddings
        """
        B, N, D = x.shape
        num_keep = int(N * (1 - self.mask_ratio)) # 196 * 0.25 = 49

        # Step 1 — generate random noise and argsort it
        noise   = torch.rand(B, N, device=x.device)   # [B, 196]
        ids_shuffle  = torch.argsort(noise, dim=1)     # [B, 196] — random order
        ids_restore  = torch.argsort(ids_shuffle, dim=1) # [B, 196] — to undo shuffle later

        # Step 2 — keep only the first num_keep indices (the "visible" patches)
        ids_keep     = ids_shuffle[:, :num_keep]       # [B, 49]

        # Step 3 — gather the visible patch embeddings
        # torch.gather picks specific indices along a dimension
        x_masked = torch.gather(
            x,
            dim=1,
            index=ids_keep.unsqueeze(-1).expand(-1, -1, D)
        )                                              # [B, 49, 192]

        # Step 4 — build a binary mask (0=visible, 1=masked)
        # used later to compute loss only on masked patches
        mask = torch.ones(B, N, device=x.device)
        mask[:, :num_keep] = 0
        # unshuffle to match original patch positions
        mask = torch.gather(mask, dim=1, index=ids_restore)  # [B, 196]

        return x_masked, mask, ids_restore

class MultiHeadAttention(nn.Module):
    def __init__(self, embed_dim=192, num_heads=3):
        super().__init__()
        self.num_heads  = num_heads
        self.head_dim   = embed_dim // num_heads
        self.scale      = self.head_dim ** -0.5

        # Projects input into Q, K, V in one shot
        self.qkv        = nn.Linear(embed_dim, embed_dim * 3)
        self.proj       = nn.Linear(embed_dim, embed_dim)

    def forward(self, x):
        B, N, D = x.shape

        # Compute Q, K, V and split into heads
        qkv = self.qkv(x)                             # [B, N, 3*192]
        qkv = qkv.reshape(B, N, 3, self.num_heads, self.head_dim)
        qkv = qkv.permute(2, 0, 3, 1, 4)             # [3, B, heads, N, head_dim]
        q, k, v = qkv[0], qkv[1], qkv[2]             # each: [B, heads, N, head_dim]

        # Scaled dot-product attention
        attn = (q @ k.transpose(-2, -1)) * self.scale # [B, heads, N, N]
        attn = attn.softmax(dim=-1)

        # Weighted sum of values
        x = (attn @ v)                                 # [B, heads, N, head_dim]
        x = x.transpose(1, 2).reshape(B, N, D)        # [B, N, 192]
        x = self.proj(x)
        return x


class TransformerBlock(nn.Module):
    def __init__(self, embed_dim=192, num_heads=3, mlp_ratio=4.0):
        super().__init__()
        self.norm1 = nn.LayerNorm(embed_dim)
        self.attn  = MultiHeadAttention(embed_dim, num_heads)
        self.norm2 = nn.LayerNorm(embed_dim)

        # Small feedforward network inside each block
        mlp_dim    = int(embed_dim * mlp_ratio)        # 192 * 4 = 768
        self.mlp   = nn.Sequential(
            nn.Linear(embed_dim, mlp_dim),
            nn.GELU(),                                 # smoother than ReLU, standard in ViTs
            nn.Linear(mlp_dim, embed_dim)
        )

    def forward(self, x):
        # Residual connections — x is added back after each sub-layer
        x = x + self.attn(self.norm1(x))
        x = x + self.mlp(self.norm2(x))
        return x


class MAEEncoder(nn.Module):
    def __init__(self, image_size=224, patch_size=16,
                 embed_dim=192, depth=6, num_heads=3):
        super().__init__()

        self.patch_embed  = PatchEmbedding(image_size, patch_size, 3, embed_dim)
        self.masking      = RandomMasking(mask_ratio=0.75)

        # CLS token — a learnable summary vector prepended to the sequence
        self.cls_token    = nn.Parameter(torch.zeros(1, 1, embed_dim))

        # Positional embeddings — tell the model where each patch is spatially
        num_patches       = self.patch_embed.num_patches
        self.pos_embed    = nn.Parameter(torch.zeros(1, num_patches + 1, embed_dim))

        # Stack of Transformer blocks
        self.blocks       = nn.ModuleList([
            TransformerBlock(embed_dim, num_heads)
            for _ in range(depth)                      # 6 layers
        ])
        self.norm         = nn.LayerNorm(embed_dim)

        self._init_weights()

    def _init_weights(self):
        # Small normal init — standard practice for ViTs
        nn.init.normal_(self.cls_token,  std=0.02)
        nn.init.normal_(self.pos_embed,  std=0.02)

    def forward(self, x):
        # Step 1 — patch embedding
        x = self.patch_embed(x)                        # [B, 196, 192]

        # Step 2 — add positional embeddings (skip CLS position)
        x = x + self.pos_embed[:, 1:, :]              # [B, 196, 192]

        # Step 3 — random masking → keep only 49 visible patches
        x, mask, ids_restore = self.masking(x)         # [B, 49, 192]

        # Step 4 — prepend CLS token
        cls_token = self.cls_token + self.pos_embed[:, :1, :]
        cls_token = cls_token.expand(x.shape[0], -1, -1)  # [B, 1, 192]
        x         = torch.cat([cls_token, x], dim=1)   # [B, 50, 192]

        # Step 5 — pass through Transformer blocks
        for block in self.blocks:
            x = block(x)
        x = self.norm(x)                               # [B, 50, 192]

        return x, mask, ids_restore

class MAEDecoder(nn.Module):
    def __init__(self, num_patches=196, embed_dim=192,
                 decoder_embed_dim=96, decoder_depth=2,
                 decoder_num_heads=3, patch_size=16):
        super().__init__()

        self.patch_size = patch_size

        # Project encoder dim (192) → decoder dim (96)
        # decoder is intentionally lightweight
        self.decoder_embed = nn.Linear(embed_dim, decoder_embed_dim)

        # Mask token — single learnable vector used for all masked positions
        self.mask_token    = nn.Parameter(torch.zeros(1, 1, decoder_embed_dim))

        # Decoder positional embeddings — all 196 patches + CLS
        self.decoder_pos_embed = nn.Parameter(
            torch.zeros(1, num_patches + 1, decoder_embed_dim)
        )

        # Lightweight transformer blocks (only 2, vs 6 in encoder)
        self.decoder_blocks = nn.ModuleList([
            TransformerBlock(decoder_embed_dim, decoder_num_heads)
            for _ in range(decoder_depth)
        ])

        self.decoder_norm = nn.LayerNorm(decoder_embed_dim)

        # Final projection — each patch token → 768 pixel values (16*16*3)
        self.decoder_pred = nn.Linear(
            decoder_embed_dim,
            patch_size * patch_size * 3            # 96 → 768
        )

        self._init_weights()

    def _init_weights(self):
        nn.init.normal_(self.mask_token,        std=0.02)
        nn.init.normal_(self.decoder_pos_embed, std=0.02)

    def forward(self, x, ids_restore):
        """
        x           : [B, 50, 192]  — encoder output (CLS + 49 visible)
        ids_restore : [B, 196]      — how to unshuffle back to original order
        """
        # Step 1 — project to decoder dimension
        x = self.decoder_embed(x)                     # [B, 50, 96]

        # Step 2 — remove CLS token before unshuffling patches
        x_patches = x[:, 1:, :]                       # [B, 49, 96]

        # Step 3 — create mask tokens for the 147 missing patches
        B          = x.shape[0]
        mask_tokens = self.mask_token.expand(
            B, ids_restore.shape[1] - x_patches.shape[1], -1
        )                                              # [B, 147, 96]

        # Step 4 — concatenate visible + mask tokens, then unshuffle
        x_full = torch.cat([x_patches, mask_tokens], dim=1)  # [B, 196, 96]
        x_full = torch.gather(
            x_full,
            dim=1,
            index=ids_restore.unsqueeze(-1).expand(-1, -1, x_full.shape[-1])
        )                                              # [B, 196, 96] — original order restored

        # Step 5 — prepend CLS token back
        cls = x[:, :1, :]                              # [B, 1, 96]
        x_full = torch.cat([cls, x_full], dim=1)       # [B, 197, 96]

        # Step 6 — add positional embeddings
        x_full = x_full + self.decoder_pos_embed       # [B, 197, 96]

        # Step 7 — transformer blocks
        for block in self.decoder_blocks:
            x_full = block(x_full)
        x_full = self.decoder_norm(x_full)             # [B, 197, 96]

        # Step 8 — predict pixel values, remove CLS token
        x_full = self.decoder_pred(x_full)             # [B, 197, 768]
        x_full = x_full[:, 1:, :]                      # [B, 196, 768] — drop CLS

        return x_full

class MAE(nn.Module):
    def __init__(self, image_size=224, patch_size=16,
                 embed_dim=192, depth=6, num_heads=3,
                 decoder_embed_dim=96, decoder_depth=2,
                 decoder_num_heads=3):
        super().__init__()

        self.patch_size = patch_size

        self.encoder = MAEEncoder(
            image_size = image_size,
            patch_size = patch_size,
            embed_dim  = embed_dim,
            depth      = depth,
            num_heads  = num_heads
        )

        self.decoder = MAEDecoder(
            num_patches       = (image_size // patch_size) ** 2,
            embed_dim         = embed_dim,
            decoder_embed_dim = decoder_embed_dim,
            decoder_depth     = decoder_depth,
            decoder_num_heads = decoder_num_heads,
            patch_size        = patch_size
        )

    def patchify(self, imgs):
        """
        Split images into patches — gives us the reconstruction target.
        imgs   : [B, 3, 224, 224]
        return : [B, 196, 768]
        """
        p  = self.patch_size                           # 16
        h  = w = imgs.shape[2] // p                   # 14
        x  = imgs.reshape(imgs.shape[0], 3, h, p, w, p)
        x  = torch.einsum('nchpwq->nhwpqc', x)        # rearrange dimensions
        x  = x.reshape(imgs.shape[0], h * w, p*p*3)   # [B, 196, 768]
        return x

    def forward(self, imgs):
        # Encode — get visible patch embeddings + mask info
        enc_output, mask, ids_restore = self.encoder(imgs)

        # Decode — reconstruct all 196 patches
        pred = self.decoder(enc_output, ids_restore)   # [B, 196, 768]

        # Reconstruction target — actual pixel values per patch
        target = self.patchify(imgs)                   # [B, 196, 768]

        # MSE loss — only on masked patches (mask=1 means masked)
        loss = (pred - target) ** 2                    # [B, 196, 768]
        loss = loss.mean(dim=-1)                       # [B, 196] — mean over pixels per patch
        loss = (loss * mask).sum() / mask.sum()        # average only over masked patches

        return loss, pred, mask

