def knn_accuracy(embeddings, labels, k=5):
    """
    For each sample, find k nearest neighbours and
    predict class by majority vote. Returns accuracy.
    """
    knn = KNeighborsClassifier(
        n_neighbors = k,
        metric      = 'cosine',    # cosine distance works best for normalized embeddings
        n_jobs      = -1           # use all CPU cores
    )

    # 5-fold cross validation — more reliable than a single train/test split
    scores = cross_val_score(knn, embeddings, labels, cv=5, scoring='accuracy')
    return scores.mean(), scores.std()


