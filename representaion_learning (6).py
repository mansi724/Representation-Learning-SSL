import torch
import torch.nn.functional as F

def alignment(embeddings, labels, alpha=2):
    """
    Measures how close embeddings of same-class samples are.
    Lower is better.
    Alignment = avg distance between positive pairs (same class)
    """
    embeddings = torch.tensor(embeddings)
    labels     = torch.tensor(labels)

    embeddings = F.normalize(embeddings, dim=1)

    total, count = 0.0, 0
    # sample a subset for speed — all pairs is too slow
    for cls in torch.unique(labels):
        idx  = (labels == cls).nonzero(as_tuple=True)[0]
        if len(idx) < 2:
            continue
        emb  = embeddings[idx]                        # all embeddings of this class
        # pairwise distances within class
        diff = emb.unsqueeze(0) - emb.unsqueeze(1)    # [N, N, D]
        dist = diff.norm(dim=2)                        # [N, N]
        # upper triangle only (avoid double counting)
        mask = torch.triu(torch.ones(len(idx), len(idx)), diagonal=1).bool()
        total += dist[mask].pow(alpha).mean().item()
        count += 1

    return total / count if count > 0 else 0.0


def uniformity(embeddings, t=2):
    """
    Measures how uniformly embeddings are spread across the unit sphere.
    Lower is better.
    Uniformity = log avg pairwise Gaussian kernel
    """
    embeddings = torch.tensor(embeddings)
    embeddings = F.normalize(embeddings, dim=1)

    # subsample 1000 points for speed
    idx        = torch.randperm(len(embeddings))[:1000]
    embeddings = embeddings[idx]

    sq_dists   = torch.pdist(embeddings, p=2).pow(2)  # pairwise squared distances
    return sq_dists.mul(-t).exp().mean().log().item()


