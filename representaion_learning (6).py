class SimCLREncoder(nn.Module):
    def __init__(self, embedding_dim=128):
        super().__init__()

        # Load pretrained ResNet18
        resnet = models.resnet18(weights = None)

        self.backbone = nn.Sequential(*list(resnet.children())[:-1])
        # now backbone outputs [B, 512, 1, 1]

        # Projection head — maps encoder output to embedding space
        self.projection_head = nn.Sequential(
            nn.Linear(512, 512),    # 512 → 512
            nn.ReLU(),
            nn.Linear(512, embedding_dim)   # 512 → 128
        )

    def forward(self, x):
        # x: [B, 3, 224, 224]
        features = self.backbone(x)         # [B, 512, 1, 1]
        features = features.flatten(1)      # [B, 512]
        embeddings = self.projection_head(features)  # [B, 128]
        return embeddings

import torch
import torch.nn.functional as F

class NTXentLoss(nn.Module):
    def __init__(self, temperature=0.5):
        super().__init__()
        self.temperature = temperature

    def forward(self, view1_emb, view2_emb):
        """
        view1_emb: [B, 128] — embeddings of first views
        view2_emb: [B, 128] — embeddings of second views
        """
        B = view1_emb.shape[0]

        # Step 1 — normalize embeddings to unit sphere
        # this makes dot product = cosine similarity
        z1 = F.normalize(view1_emb, dim=1)   # [B, 128]
        z2 = F.normalize(view2_emb, dim=1)   # [B, 128]

        # Step 2 — concatenate all embeddings together
        # [z1_1, z1_2, ..., z1_B, z2_1, z2_2, ..., z2_B]
        z  = torch.cat([z1, z2], dim=0)      # [2B, 128]

        # Step 3 — compute similarity between every pair
        # dot product of every embedding with every other
        sim = torch.mm(z, z.T) / self.temperature  # [2B, 2B]

        # Step 4 — remove self similarity (diagonal)
        # an image shouldn't be compared to itself
        mask = torch.eye(
            2 * B,
            dtype=torch.bool,
            device=sim.device
        )
        sim.masked_fill_(mask, float('-inf'))

        # Step 5 — build labels
        # for image i in first half, its positive is i + B (second half)
        # for image i in second half, its positive is i - B (first half)
        labels = torch.cat([
            torch.arange(B, 2 * B , device = sim.device),   # first half points to second half
            torch.arange(0, B , device = sim.device)        # second half points to first half
        ])                            # [2B]

        # Step 6 — cross entropy loss
        # for each embedding, treat positive as the correct class
        loss = F.cross_entropy(sim, labels)
        return loss

