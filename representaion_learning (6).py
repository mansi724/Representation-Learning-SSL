# ── We need a special MAE that returns the masked image too ──
def reconstruct_samples(mae_model, img_dir, num_samples=5):

    # Simple transform — same as eval
    transform = transforms.Compose([
        transforms.Resize((224, 224)),
        transforms.ToTensor(),
        transforms.Normalize(mean=[0.485, 0.456, 0.406],
                             std=[0.229, 0.224, 0.225])
    ])

    # Inverse normalize — to convert tensor back to viewable image
    inv_normalize = transforms.Normalize(
        mean=[-0.485/0.229, -0.456/0.224, -0.406/0.225],
        std=[1/0.229, 1/0.224, 1/0.225]
    )

    image_files = sorted([f for f in os.listdir(img_dir)
                         if f.endswith(('.jpg', '.jpeg', '.png'))])[:num_samples]

    fig, axes = plt.subplots(num_samples, 3, figsize=(12, 4 * num_samples))
    fig.suptitle('MAE Reconstruction', fontsize=16)

    mae_model.eval()

    for i, img_file in enumerate(image_files):
        # Load image
        img_path = os.path.join(img_dir, img_file)
        img      = Image.open(img_path).convert('RGB')
        tensor   = transform(img).unsqueeze(0).to(device)  # [1, 3, 224, 224]

        with torch.no_grad():
            loss, pred, mask = mae_model(tensor)

        # ── Reconstruct the masked image ──────────────────
        # pred   : [1, 196, 768] — predicted pixel values per patch
        # mask   : [1, 196]      — 1=masked, 0=visible
        p   = mae_model.patch_size                          # 16
        h   = w = 224 // p                                  # 14

        # Unpatchify prediction → full image tensor
        pred_img = pred.reshape(1, h, w, p, p, 3)
        pred_img = torch.einsum('nhwpqc->nchpwq', pred_img)
        pred_img = pred_img.reshape(1, 3, 224, 224)        # [1, 3, 224, 224]

        # Build masked image — black out masked patches
        mask_vis = mask.reshape(1, h, w)                    # [1, 14, 14]
        mask_vis = mask_vis.unsqueeze(1)                    # [1, 1, 14, 14]
        mask_vis = mask_vis.repeat(1, 3, 1, 1)             # [1, 3, 14, 14]
        mask_vis = torch.nn.functional.interpolate(
            mask_vis.float(), scale_factor=p               # upscale to [1, 3, 224, 224]
        )

        # Apply mask to original — zero out masked regions
        masked_img = tensor * (1 - mask_vis)               # visible patches only

        # ── Convert tensors to numpy for plotting ─────────
        def to_img(t):
            t = inv_normalize(t.squeeze(0).cpu())
            t = torch.clamp(t, 0, 1)
            return t.permute(1, 2, 0).numpy()

        orig_np    = to_img(tensor)
        masked_np  = to_img(masked_img)
        pred_np    = to_img(pred_img)

        # ── Plot row ───────────────────────────────────────
        axes[i][0].imshow(orig_np)
        axes[i][0].set_title('Original')
        axes[i][0].axis('off')

        axes[i][1].imshow(masked_np)
        axes[i][1].set_title('Masked (75% hidden)')
        axes[i][1].axis('off')

        axes[i][2].imshow(pred_np)
        axes[i][2].set_title('MAE Reconstruction')
        axes[i][2].axis('off')

    plt.tight_layout()
    plt.savefig(f"{PROJECT_DIR}/mae_reconstruction.png", dpi=150, bbox_inches='tight')
    plt.show()
    print("Reconstruction plot saved ✅")


