import numpy as np
from sklearn.metrics.pairwise import cosine_similarity

def retrieval_precision_at_k(embeddings, labels, k=5):
    """
    For each image, find top-K most similar images.
    Precision@K = fraction of top-K that share the same class.
    """
    # Compute full cosine similarity matrix
    sim_matrix = cosine_similarity(embeddings)   # [N, N]

    # Set diagonal to -1 so image doesn't retrieve itself
    np.fill_diagonal(sim_matrix, -1)

    precisions = []
    for i in range(len(labels)):
        # Get indices of top-K most similar images
        top_k_idx      = np.argsort(sim_matrix[i])[::-1][:k]
        top_k_labels   = labels[top_k_idx]

        # Precision = how many of top-K share same class
        correct        = (top_k_labels == labels[i]).sum()
        precisions.append(correct / k)

    return np.mean(precisions)


print("Running image retrieval evaluation...")
