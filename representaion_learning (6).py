from sklearn.cluster import KMeans
from sklearn.metrics import silhouette_score, davies_bouldin_score

def clustering_metrics(embeddings, n_clusters=25):
    """
    Fit KMeans and measure cluster quality.
    Silhouette: higher is better  (range -1 to 1)
    Davies-Bouldin: lower is better (range 0 to ∞)
    """
    km = KMeans(n_clusters=n_clusters, random_state=42, n_init=10)
    cluster_labels = km.fit_predict(embeddings)

    sil = silhouette_score(embeddings, cluster_labels, metric='cosine')
    db  = davies_bouldin_score(embeddings, cluster_labels)

    return sil, db

