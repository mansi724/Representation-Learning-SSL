class MAEEncoderInference(nn.Module):
    def __init__(self, mae_encoder):
        super().__init__()
        self.patch_embed = mae_encoder.patch_embed
        self.cls_token   = mae_encoder.cls_token
        self.pos_embed   = mae_encoder.pos_embed
        self.blocks      = mae_encoder.blocks
        self.norm        = mae_encoder.norm

    def forward(self, x):
        # Patch embedding
        x = self.patch_embed(x)                        # [B, 196, 192]

        # Add positional embeddings
        x = x + self.pos_embed[:, 1:, :]              # [B, 196, 192]

        # Prepend CLS token — no masking this time
        cls_token = self.cls_token + self.pos_embed[:, :1, :]
        cls_token = cls_token.expand(x.shape[0], -1, -1)
        x         = torch.cat([cls_token, x], dim=1)  # [B, 197, 192]

        # Pass through transformer blocks
        for block in self.blocks:
            x = block(x)
        x = self.norm(x)                               # [B, 197, 192]

        # Return CLS token only — image level representation
        return x[:, 0, :]                              # [B, 192]

